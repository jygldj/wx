简朴思
散文｜乙巳年五月十八

简朴思
——在粗粝中触摸永恒
清晨推窗，竹影在青砖上写狂草。檐角垂落的蛛网兜住半片槐花，风过时，花与网皆成水墨。这便是简朴的启蒙课——天地从不以金箔裹石，却让苔痕在墙根绣出年轮。
陶渊明采菊东篱时，衣襟沾满草籽与晨露，却比任何锦袍都接近永恒。他在《归去来兮辞》中写道：“悦亲戚之情话，乐琴书以消忧”，何尝不是用最朴素的声色构筑精神堡垒？王维辋川别业的竹坞云房，实则是将自然裁剪成窗框里的画轴。那些被现代人斥为“破败”的残荷枯柳，在八大山人的笔下，化作“墨点无多泪点多”的绝响。
灶台上煨着陈年陶罐，腊肉与萝卜在蒸汽中达成和解。这让我想起苏轼在《菜羹赋》中自嘲：“何用和銮，而自煮此无盐之汤”。当现代人追逐分子料理的幻术时，粗陶碗里的荠菜豆腐羹，正以最原始的方式完成对味觉的启蒙。
袁枚《随园食单》记载“有味者使之无味”的烹饪哲学，恰似禅宗“平常心是道”的变奏。京都怀石料理将豆腐切成月牙状，配以松针熏制的鲣鱼花，看似精致，实则延续着“和敬清寂”的简素之道。而贵州侗寨的酸汤鱼，用木桶发酵出的酸味，比任何酱料都更接近大地的本味。
徽州老宅的天井承接四时风雨，雨水顺着瓦当滴落青石，日日年年刻写着《漱玉集》。这种“四水归堂”的格局，暗合《园冶》所言“虽由人作，宛自天开”的智慧。反观现代玻璃幕墙的冰冷森林，那些用LED灯光模拟的“星空顶”，终究照不亮灵魂的暗角。
日本安藤忠雄的“光之教堂”，用混凝土与十字光束完成对简朴的极致诠释。而苏州博物馆的片石假山，贝聿铭以米芾画意重构山水，在钢筋水泥间保留着“瘦皱漏透”的东方美学密码。真正的建筑大师，都懂得在留白处安放云影。
祖父的铜壶总在寅时敲响更漏，壶嘴飘出的茶烟，与北斗的斗柄共绘时间的螺旋。这种农耕文明的时间刻度，比手机日历更接近生命的本相。陶弘景在《答谢中书书》中描绘的“晓雾将歇，猿鸟乱鸣”，实则是用二十四节气编织的时光锦缎。
当都市人困在“碎片化”“即时性”的牢笼里，云南哈尼族的“十月太阳历”仍在梯田上流转。他们用不同颜色的糯稻绘制大地画，让农耕成为最隆重的时光祭祀。这种对季节的虔诚，比任何智能手环都更能丈量生命的质地。
东京表参道的极简主义橱窗里，一盏清水与枯枝构成“侘寂”的美学标本。这让我想起老子“大巧若拙”的箴言——当消费主义将欲望包装成必需品时，真正的奢侈恰是放下。
景德镇陶艺家李见深用五年时间复原宋代影青瓷，却故意保留窑变的瑕疵。这些“不完美”的器物，反而成为当代人对抗异化的图腾。就像《菜根谭》所言：“风来疏竹，风过而竹不留声”，简朴不是苦行，而是让灵魂在物的缝隙中自由呼吸。
暮色漫过窗棂时，我看见陶罐里的野菊悄然绽放。那些被现代性碾碎的时光，正在粗陶的肌理间重新凝结成琥珀。或许真正的奢华，不过是学会在菜根的苦涩里，尝出岁月的回甘。


