三城食事记
散文｜乙巳年四月十五日

一、武威：古城的胃
凉州的清晨是从三套车的热气里醒来的。
行面如汉简，一根根摊开，是未写完的《凉州词》。卤肉肥瘦相间，肥处似雷台汉墓的铜马鞍，瘦处如西夏碑上的刻痕。
红枣茯茶滚烫，粗瓷碗底沉着几颗皱缩的枣，像祖父烟锅里的余烬。喝到最后，茶渣在舌尖化作祁连山的雪粒。
偶尔有卖腊羊肉夹馍的回民老汉路过，馍皮烙得焦黄，肉塞得满溢——咬下去的瞬间，仿佛咬住了古凉州的落日。
二、嘉峪关：钢铁与风沙的滋味
在钢城的日子，羊肉粉汤是寒夜的铠甲。
羊骨熬汤，撒一把胡椒，辣得人脊背发汗，如烽燧上的戍卒喝退胡风。
烧壳子硬如城砖，掰开却酥软，甜味是讨赖河冰层下渗出的暗流。
有时下班路过市场，买一份酿皮子。醋汁泼辣，面筋吸饱了关外的风，嚼着嚼着，忽然尝出明代戍卒偷藏的半口乡愁。
三、重庆：麻辣里的黄昏
而今在渝州，小面的麻辣轰然炸响，却炸不散记忆里的西北味。
