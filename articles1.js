道显则玄隐，如日月经天；玄彰则道微，若辰宿列汉。道者其纲，玄者其纬，经纬交络而人文以成。昔伏羲画卦，文王系辞，皆以道玄为枢。今愚子承名，当效天行健，法地势坤，明明德于躬行，通玄览于静观。
