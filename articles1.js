吾母咯血持家，实抚五子。一男四女，吾居次列。劬劳育雏，痼疾暗生。父亲远赴酒钢谋生，母独扛门祚之重。同宗王氏，恃强凌寡：分粮克扣，工分薄算，长姊劳作最多而计分最少，稚子吞声，慈母饮恨。
朔风怒号之日，母亲抱恙，跋涉求医，幼丁伴行，以免孤单。深洼险渠，瘦影踉跄，白犬通灵，蹑影相随。至医馆，慈医陈氏见母囊空，免其五角欠资。归途风沙蔽目，母力竭倚坎，白犬竟衔生穗相献——畜类有情，怎知人间烟火？
麦浪翻金时节，分粮场上，母遭欺昏厥。幸得五奶恻隐，唤邻人舁归陋室，掐人中灌温水，至亥时方苏。此般桩桩血泪，实难尽述。
丙辰冬，父获单位嘉奖，举家迁农场。自此廪实仓满，庚申年吾金榜题名，越岁两妹招工入岗，乙丑时家道中兴。然正欲反哺，母竟溘然长往于盂兰盆节，佛号声中魂归净土。
母逝十载，悲潮难遏。或醉后嚎啕，或夜半呜咽，贤妻默抚衾被，容我恣情。今终悟得：慈母菩萨心肠，尘缘已了当乘莲西去。
吾母之德，山高水长：
如黄河奔涌，泽被后世；
似泰山巍峨，荫庇子孙。
大江南北，瓜瓞绵延；
四十寒暑，音容宛在。
哀哀吾母！
毕生茹苦，未享甘饴；
儿辈立身，未及奉养。
精卫难填思亲海，春晖永耀沐恩心。
伏惟尚飨！
