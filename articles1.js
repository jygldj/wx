诗｜乙巳年五月十五日

第一珠·重庆北站
朝天门的汽笛泡进火锅油
长江与嘉陵江在此结拜
行李箱滚轮碾过的水洼
倒映出四十年前粮票的暗纹
第二珠·合川钓鱼城
悬崖上的箭垛仍在耳鸣
蒙哥汗的叹息长出青苔
钓鱼台下三江交汇处
有块圆石是母亲揉面的案板
第三珠·南充嘉陵江大桥
铁桥肋骨间卡着纤夫号子
白塔影子在波光里抄经
对岸晾晒的万匹绸缎
飘成大学录取通知书的衬里
第四珠·广元剑门关
李白叹过的鸟道缩成高铁线
崖壁石刻被时速250公里翻译
守关将士的铜盔坠入江中
变作幺妹耳环上的松绿石
第五珠·陇南官鹅沟
藏羌的碉楼是未拆的积木
雪水在保温杯里酿青稞酒
岩羊跃过的刹那
整条峡谷突然学父亲叹息
第六珠·定西黄土梯田
犁沟里的冰雹是去年未化的盐
旱柳枝在风中吼秦腔
地膜覆盖的垄上
有工龄买断时的指纹
第七珠·兰州黄河铁桥
羊皮筏子载走中山桥的锈斑
白塔山公园的旋转椅停摆
年轻时在此遗落的书包
正被浪花推成新的河心岛


