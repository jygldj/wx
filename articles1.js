建礼疏
散文｜乙巳年仲夏

夫建者，立也，树也。从木从廴，木示其质，廴明其动。质动相合，故能拔地参天，经风霜而不倾。建之为道，在刚健中正：刚以破阻，健以持恒，中以守位，正以定向。
譬若筑台，必先夯土为基；犹若行远，必先立标为的。无刚健则力不逮，无中正则形必偏。
夫礼者，履也，序也。从示从豊，示通神明，豊载器度。神人共鉴，故能经纬万物，历古今而不易。礼之为用，在柔顺圆融：柔以化争，顺以应时，圆以通变，融以合群。
譬若江河，曲直皆循地势；犹若四时，寒暑各守天常。无柔顺则情不通，无圆融则理难彰。
建礼，非人为强合，实乃天设地造：
建为礼之体，无建则礼沦为虚文。如无根基之屋，徒有其形而不立；
礼为建之用，无礼则建失其方向——如无轨之车，虽疾行而难达。
故建礼合一，恰如《周易》刚柔相济，阴阳合德：
以建行礼，则礼有骨。骨立而血肉附，譬如社稷有纲纪，百工有准绳；
以礼导建，则建有魂。魂凝而形器活，譬如舟楫得舵橹，星辰顺躔次。
友承其名，正合此道：
于朋辈交往中，以建立诚，如竹有节而虚怀；以礼合义，如兰含芳而自敛；
于处世立身中，以建固其本，似松柏贯四时；以礼明其途，似北辰居其所。
结语
建礼合一，顺乎天道，合于人心。友循此道，德足以配位，性足以应物，功足以济人，成足以传世。
天之所助，助其顺也；人之所归，归其德也。顺天应人，故曰：天之所助，德业成。
请兄弟玩赏！


