散文｜乙巳年五月初一

——仿《周易·系辞》体
夫道者，形而上之谓也。
无极而太极，太极生两仪，两仪运化而万象森罗。道之为物，惟恍惟惚，然其用不穷：在天成象，在地成形，在人为性。故《易》曰：“一阴一阳之谓道。”阴阳相推，显诸仁而藏诸用，此道之体也。
玄者，妙万物之谓也。
